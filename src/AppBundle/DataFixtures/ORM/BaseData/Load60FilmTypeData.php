            'Whiteout' => 'Graphics',
            'Yamato SH2PTYA' => 'Graphics',
        ];

        foreach ($filmTypes as $filmType => $category) {
            $filmTypeObj = new FilmType();
            $filmTypeObj
                ->setDescription($filmType)
                ->setFilmCategory($this->getReference('category-'.str_replace(' ', '-', strtolower($category))))
            ;

            $manager->persist($filmTypeObj);
            $this->addReference('film-type-'.str_replace(' ', '-', strtolower($filmType)), $filmTypeObj);
        }

        $manager->flush();
    }

